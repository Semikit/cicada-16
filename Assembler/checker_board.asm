    .boot_anim_audio 0x04
    .title "Static-Image"
    .developer "Your-Name"
    .version 1
    .mapper 0
    .rom_size 1        ; 32KB
    .ram_size 0        ; No RAM
    .interrupt_mode 0  ; ROM-based vectors
    .hardware_rev 0
    .region 0          ; All regions
.header_end

; --- Interrupt Vector Table ---
.interrupt_table
    .word start              ; RESET
    .word default_handler    ; Bus Error
    .word default_handler    ; Illegal Instruction
    .word default_handler    ; Protected Memory
    .word default_handler    ; Stack Overflow
    .word vblank_handler     ; V-Blank
    .word hblank_handler    ; H-Blank
    .word lyc_handler    ; LYC == LY
    .word timer_handler    ; Timer
    .word timer_handler    ; Timer
    .word serial_handler    ; Serial
    .word link_status_handler    ; Link Status
    .word joypad_handler    ; Joypad
.table_end

; --- Constants ---
.define VRAM_BASE 0x9000      ; Start of VRAM window
.define CRAM_BASE 0xF200      ; Color RAM (palette)
.define OAM_BASE  0xF400      ; Object Attribute Memory
.define IE_ADDR 0xF020	      ; interrupt enable register
.define IF_ADDR 0xF021	      ; interrupt flag register
.define JOYPAD_ADDR 0xF006    ; joypad register
.define MOVE_SPEED_ADDR 0xFE02
.define VBLANK_DETECT 0xFE04
.define SPRITE_PALETTE_ADDR 0xFE06  ; Current palette for sprite 0
.define DMA_SRC_ADDR 0xF00A
.define DMA_DST_ADDR 0xF00C
.define DMA_LEN_ADDR 0xF00E
.define DMA_CTL_ADDR 0xF010

; Timer Registers
.define DIV0_ADDR 0xF002      ; DIV counter byte 0
.define DIV1_ADDR 0xF003      ; DIV counter byte 1
.define DIV2_ADDR 0xF004      ; DIV counter byte 2
.define DIV3_ADDR 0xF005      ; DIV counter byte 3
.define TIMA_ADDR 0xF007      ; Timer counter
.define TMA_ADDR  0xF008      ; Timer modulo (reload value)
.define TAC_ADDR  0xF009      ; Timer control

; PPU Registers
.define LCDC      0xF040      ; LCD Control
.define STAT      0xF041      ; PPU STAT
.define BG_MODE   0xF04A      ; Background mode (tilemap size)
.define BG_TMB    0xF04B      ; Background tilemap base
.define WIN_TMB   0xF04C      ; Window tilemap base
.define SCX0      0xF043      ; BG0 X scroll
.define SCY0      0xF042      ; BG0 Y scroll
.define SCX1      0xF045      ; BG1 X scroll
.define SCY1      0xF044      ; BG1 Y scroll
.define WINX      0xF047      ; Window X position
.define WINY      0xF046      ; Window Y position

; VRAM Bank register
.define VRAM_BANK 0xF014

; Tilemap settings
.define TILEMAP_WIDTH  32     ; 32 tiles wide
.define TILEMAP_HEIGHT 32     ; 32 tiles high

; ========================================
; Entry Point
; ========================================
start:
    ; Initialize stack pointer
    ; LDI R7, 0xCFFF

    ; Disable interrupts during setup
    DI

    ; Turn off PPU while we set up VRAM
    LDI R0, 0
    ST.b (LCDC), R0

    ; Copy palette data to CRAM
    CALL load_palette

    ; Select VRAM bank 0
    LDI R0, 0
    ST.b (VRAM_BANK), R0

    ; Load tile data into VRAM
    CALL load_tiles

    ; Copy tilemap for BG0 (checkerboard)
    CALL load_tilemap_bg0

    ; Copy tilemap for BG1 (stripes)
    CALL load_tilemap_bg1

    ; Copy window tiles to BG0 tilemap
    CALL load_window_tiles

    ; Setup sprites in OAM
    CALL setup_sprites

    ; Configure PPU
    CALL setup_ppu

    LDI R0, 0
    ST.b (VBLANK_DETECT), R0

    ; Setup timer for palette changes
    ; We want to change palette roughly every 0.5 seconds
    ; At 16.777216 MHz, we need to select appropriate DIV bit and TIMA reload

    ; Let's use CLK_SEL = 16 (bit 16 of DIV counter)
    ; Bit 16 toggles every 2^16 = 65,536 cycles
    ; At 16.777216 MHz, that's ~3.9ms per toggle
    ; Falling edge every ~7.8ms

    ; To get ~0.5 second intervals:
    ; 0.5 sec = 8,388,608 cycles
    ; 8,388,608 / 65,536 = 128 falling edges
    ; So we set TIMA to overflow after 128 increments (256 - 128 = 128)

    LDI R0, 128           ; TMA reload value
    ST.b (TMA_ADDR), R0

    LDI R0, 128           ; Initial TIMA value
    ST.b (TIMA_ADDR), R0

    LDI R0, 0x2E          ; TAC: Enable timer (bit 5), CLK_SEL = 16 (bits 4-0)
    ST.b (TAC_ADDR), R0

    ; Initialize sprite palette tracker
    LDI R0, 0             ; Start with palette 0
    ST.b (SPRITE_PALETTE_ADDR), R0

    ; Enable V-Blank and Timer interrupts
    LDI R0, 0x29          ; Enable V-Blank (bit 0) and Timer (bit 3) interrupts
    ST.b (IE_ADDR), R0     ; Write to IE register
    EI

    LDI R5, 3
    ST (MOVE_SPEED_ADDR), R5

    LDI R5, 1
    LDI R4, 1
    ; number of frames to wait
    LDI R3, 0xF
    ST (0xFE00), R3

    ; Main loop - just wait for V-Blank
main_loop:
    DEC R3
    JRNZ continue
    LD R3, (0xFE00)
    CMPI R4, 1
    JRNZ move_up
move_down:
    LD.b R4, (SCY0)
    SUB R4, R5
    ST.b (SCY0), R4
    LDI R4, 0
    JR continue
move_up:
    LD.b R4, (SCY0)
    ADD R4, R5
    ST.b (SCY0), R4
    LDI R4, 1
continue:
    CALL wait_for_vblank

    PUSH R0
    PUSH R1
    PUSH R2
    LDI R1, OAM_BASE
    ; LEA R1, (R0, 24)
    CALL move_sprite
    POP R2
    POP R1
    POP R0

    JMP main_loop

; ========================================
; Load Palette Data to CRAM
; ========================================
load_palette:
    PUSH R0
    PUSH R1
    PUSH R2
    PUSH R3

    ; Source: palette_data in ROM
    LDI R0, palette_data
    LDI R1, 0 ; destination: slot 0
    LDI R2, 64 ; number of slots: 64
    LDI R3, 0x1D ; DMA_MODE: 3, VRAM_SAFE: 1, ADDR_MODE: 0, DMA_START: 1

    ST (DMA_SRC_ADDR), R0
    ST (DMA_DST_ADDR), R1
    ST (DMA_LEN_ADDR), R2
    ST.b (DMA_CTL_ADDR), R3

    POP R3
    POP R2
    POP R1
    POP R0
    RET

; ========================================
; Load Tile Graphics into VRAM
; ========================================
load_tiles:
    PUSH R0
    PUSH R1
    PUSH R2
    PUSH R3

    ; Source: tile_data in ROM
    LDI R0, tile_data

    ; Destination: VRAM base (tile graphics start at 0x9000)
    LDI R1, VRAM_BASE

    LDI R2, 192
    LDI R3, 0x04

    ST (DMA_LEN_ADDR), R2
    ST.b (DMA_CTL_ADDR), R3

    ST (DMA_SRC_ADDR), R0
    ST (DMA_DST_ADDR), R1

    ; start transfer
    SET (DMA_CTL_ADDR), 0

    POP R3
    POP R2
    POP R1
    POP R0
    RET

; ========================================
; Load Tilemap for BG0 (Checkerboard)
; ========================================
load_tilemap_bg0:
    PUSH R0
    PUSH R1
    PUSH R2
    PUSH R3

    ; Source: tilemap_bg0_data in ROM
    LDI R0, tilemap_bg0_data
    LDI R1, 2 ; destination: slot 2
    LDI R2, 1 ; number of slots: 1
    LDI R3, 0x15 ; DMA_MODE: 2, VRAM_SAFE: 1, ADDR_MODE: 0, DMA_START: 1

    ST (DMA_SRC_ADDR), R0
    ST (DMA_DST_ADDR), R1
    ST (DMA_LEN_ADDR), R2
    ST.b (DMA_CTL_ADDR), R3

    POP R3
    POP R2
    POP R1
    POP R0
    RET

; ========================================
; Load Tilemap for BG1 (Vertical stripes)
; ========================================
load_tilemap_bg1:
    PUSH R0
    PUSH R1
    PUSH R2
    PUSH R3

    ; Source: tilemap_bg0_data in ROM
    LDI R0, tilemap_bg1_data
    LDI R1, 4 ; destination: slot 2
    LDI R2, 1 ; number of slots: 1
    LDI R3, 0x15 ; DMA_MODE: 2, VRAM_SAFE: 1, ADDR_MODE: 0, DMA_START: 1

    ST (DMA_SRC_ADDR), R0
    ST (DMA_DST_ADDR), R1
    ST (DMA_LEN_ADDR), R2
    ST.b (DMA_CTL_ADDR), R3

    POP R3
    POP R2
    POP R1
    POP R0
    RET

; ========================================
; Load Window Tilemap to VRAM
; ========================================
load_window_tiles:
    PUSH R0
    PUSH R1
    PUSH R2
    PUSH R3
    PUSH R4

    ; Source: window_tile_data in ROM
    LDI R0, window_tile_data
    LDI R1, 6 ; destination: slot 2
    LDI R2, 1 ; number of slots: 1
    LDI R3, 0x15 ; DMA_MODE: 2, VRAM_SAFE: 1, ADDR_MODE: 0, DMA_START: 1

    ST (DMA_SRC_ADDR), R0
    ST (DMA_DST_ADDR), R1
    ST (DMA_LEN_ADDR), R2
    ST.b (DMA_CTL_ADDR), R3

    POP R4
    POP R3
    POP R2
    POP R1
    POP R0
    RET

; ========================================
; Setup Sprites (4 moving sprites)
; ========================================
setup_sprites:
    PUSH R0
    PUSH R1
    PUSH R2

    LDI R1, OAM_BASE

    ; Sprite 0: Top-left, tile 0, palette 3 (blue)
    LDI R0, 40            ; byte 0: Y position
    ST.b (R1)+, R0
    LDI R0, 60            ; byte 1: X position
    ST.b (R1)+, R0
    LDI R0, 0             ; byte 2: Tile size/shape (square 8x8)
    ST.b (R1)+, R0
    LDI R0, 1             ; byte 3: Tile index
    ST.b (R1)+, R0
    LDI R0, 0x02          ; byte 4: Attributes: palette 3 (bits 2-0 = 011), no priority
    ST.b (R1)+, R0
    LDI R0, 0             ; byte 5: reserved
    ST.b (R1)+, R0
    LDI R0, 0             ; byte 6: reserved
    ST.b (R1)+, R0
    LDI R0, 0             ; byte 7: Reserved
    ST.b (R1)+, R0

    ; Sprite 1: Top-right, tile 1, palette 3 (blue)
    LDI R0, 40            ; byte 0: Y position
    ST.b (R1)+, R0
    LDI R0, 180            ; byte 1: X position
    ST.b (R1)+, R0
    LDI R0, 0             ; byte 2: Tile size/shape (square 8x8)
    ST.b (R1)+, R0
    LDI R0, 1             ; byte 3: Tile index
    ST.b (R1)+, R0
    LDI R0, 0x03          ; byte 4: Attributes: palette 3 (bits 2-0 = 011), no priority
    ST.b (R1)+, R0
    LDI R0, 0             ; byte 5: reserved
    ST.b (R1)+, R0
    LDI R0, 0             ; byte 6: reserved
    ST.b (R1)+, R0
    LDI R0, 0             ; byte 7: Reserved
    ST.b (R1)+, R0

    ; Sprite 2: Bottom-left, tile 2, palette 3 (blue) high priority
    LDI R0, 120            ; byte 0: Y position
    ST.b (R1)+, R0
    LDI R0, 60            ; byte 1: X position
    ST.b (R1)+, R0
    LDI R0, 0             ; byte 2: Tile size/shape (square 8x8)
    ST.b (R1)+, R0
    LDI R0, 1             ; byte 3: Tile index
    ST.b (R1)+, R0
    LDI R0, 0x83          ; byte 4: Attributes: palette 3 (bits 2-0 = 011), no priority
    ST.b (R1)+, R0
    LDI R0, 0             ; byte 5: reserved
    ST.b (R1)+, R0
    LDI R0, 0             ; byte 6: reserved
    ST.b (R1)+, R0
    LDI R0, 0             ; byte 7: Reserved
    ST.b (R1)+, R0

    ; Sprite 3: Bottom-right, tile 3, palette 3 (blue)
    LDI R0, 120            ; byte 0: Y position
    ST.b (R1)+, R0
    LDI R0, 180            ; byte 1: X position
    ST.b (R1)+, R0
    LDI R0, 0             ; byte 2: Tile size/shape (square 8x8)
    ST.b (R1)+, R0
    LDI R0, 1             ; byte 3: Tile index
    ST.b (R1)+, R0
    LDI R0, 0x01          ; byte 4: Attributes: palette 3 (bits 2-0 = 011), no priority
    ST.b (R1)+, R0
    LDI R0, 0             ; byte 5: reserved
    ST.b (R1)+, R0
    LDI R0, 0             ; byte 6: reserved
    ST.b (R1)+, R0
    LDI R0, 0             ; byte 7: Reserved
    ST.b (R1)+, R0

    POP R2
    POP R1
    POP R0
    RET

; ========================================
; Setup PPU Registers
; ========================================
setup_ppu:
    PUSH R0

    ; Set background mode
    ; BG_MODE bits 3-0 control BG0 size, bits 7-4 control BG1 size
    ; 0000 = 32x32 tiles (256x256 pixels)
    LDI R0, 0x00
    ST.b (BG_MODE), R0

    ; Set tilemap base addresses
    ; BG_TMB: bits 3-0 = BG0 slot, bits 7-4 = BG1 slot
    ; BG0 at slot 2 (0x1000), BG1 at slot 4 (0x2000)
    LDI R0, 0x42          ; Binary: 0100 0010
    ST.b (BG_TMB), R0

    ; Set window tilemap base address
    ; WIN_TMB: bits 3-0 = Window slot
    ; Window at slot 6 (0x3000)
    LDI R0, 0x06
    ST.b (WIN_TMB), R0

    ; Set BG0 scroll to 0,0
    LDI R0, 0
    ST.b (SCX0), R0
    ST.b (SCY0), R0

    ; Set BG1 scroll offset for visual distinction
    LDI R0, 64
    ST.b (SCX1), R0
    LDI R0, 64
    ST.b (SCY1), R0

    ; Set Window position (top-right corner area)
    ; WINX/WINY specify the screen position where the window appears
    LDI R0, 0           ; Window X position
    ST.b (WINX), R0
    LDI R0, 0            ; Window Y position
    ST.b (WINY), R0

    ; Enable vblank interrupt
    ; STAT bit 4 = vblank enable
    SET (STAT), 4

    ; Enable PPU, BG0, BG1, Window, and Sprites
    ; LCDC bit 7 = PPU enable
    ; LCDC bit 6 = Sprites enable
    ; LCDC bit 5 = BG1 enable
    ; LCDC bit 4 = BG0 enable
    ; LCDC bit 0 = Window enable
    LDI R0, 0xD1          ; Binary: 11010001
    ST.b (LCDC), R0

    POP R0
    RET

; ========================================
; Halt until vblank interrupt
; ========================================
wait_for_vblank:
    HALT
    BIT (VBLANK_DETECT), 0
    JRZ wait_for_vblank
    RES (VBLANK_DETECT), 0
    RET

; ========================================
; Read joypad and move a sprite
;
; R1 = base address of sprite in VRAM to move
; clobbered: R0, R2
; ========================================
move_sprite:
    LD R2, (MOVE_SPEED_ADDR)
    LDI R0, 0x10
    ST.b (JOYPAD_ADDR), R0

    LD.b R0, (R1)

    BIT (JOYPAD_ADDR), 2
    JRNZ skip_up
    SUB R2
skip_up:

    BIT (JOYPAD_ADDR), 3
    JRNZ skip_down
    ADD R2
skip_down:

    ST.b (R1)+, R0
    LD.b R0, (R1)

    BIT (JOYPAD_ADDR), 0
    JRNZ skip_right
    ADD R2
skip_right:

    BIT (JOYPAD_ADDR), 1
    JRNZ skip_left
    SUB R2
skip_left:

    ST.b (R1), R0

    RET

; ========================================
; Interrupt Handlers
; ========================================
vblank_handler:
    SET (IF_ADDR), 0
    SET (VBLANK_DETECT), 0
    RETI

hblank_handler:
    RES (IF_ADDR), 1
    RETI

lyc_handler:
    RES (IF_ADDR), 2
    RETI

timer_handler:
    ; Clear the timer interrupt flag
    RES (IF_ADDR), 3

    ; Save registers we'll use
    PUSH R0
    PUSH R1
    PUSH R2

    ; Read current palette
    LD.b R0, (SPRITE_PALETTE_ADDR)

    ; Increment palette (cycle through 0-3)
    INC R0
    ANDI R0, 0x03         ; Mask to keep in range 0-3

    ; Store new palette
    ST.b (SPRITE_PALETTE_ADDR), R0

    ; Update sprite 0's attributes in OAM
    ; Sprite 0 attributes are at OAM_BASE + 4 (byte 4 of first sprite)
    LDI R1, OAM_BASE
    ADDI R1, 4            ; Point to attributes byte

    ; Read current attributes
    LD.b R2, (R1)

    ; Clear old palette bits (bits 2-0)
    ANDI R2, 0xF8         ; Keep bits 7-3, clear bits 2-0

    ; Set new palette
    OR R2, R0             ; Combine with new palette value

    ; Write back to OAM
    ST.b (R1), R2

    ; Restore registers
    POP R2
    POP R1
    POP R0

    RETI

serial_handler:
    RES (IF_ADDR), 4
    RETI

joypad_handler:
    RES (IF_ADDR), 5
    RETI

link_status_handler:
    RES (IF_ADDR), 6
    RETI

default_handler:
    RETI

